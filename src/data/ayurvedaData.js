// ── Day-of-year rotation ───────────────────────────────────────
export function getTodayAyurvedaTip(date = new Date()) {
  const start = new Date(date.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((date - start) / 86400000);
  return AYURVEDA_TIPS[dayOfYear % AYURVEDA_TIPS.length];
}

// ── Sources — classical Ayurveda compendia ─────────────────────
// Charaka Samhita and Ashtanga Hridayam are the two most commonly
// cited sources here. Bhavaprakasha (16th c.) is the standard modern
// reference. All three are catalogued under sanskritdocuments.org's
// Ayurveda listing.
export const AYURVEDA_SOURCES = {
  'Charaka Samhita':       'https://sanskritdocuments.org/sanskrit/ayurveda/',
  'Sushruta Samhita':      'https://sanskritdocuments.org/sanskrit/ayurveda/',
  'Ashtanga Hridayam':     'https://sanskritdocuments.org/sanskrit/ayurveda/',
  'Bhavaprakasha':         'https://sanskritdocuments.org/sanskrit/ayurveda/',
  'Yoga Sutra':            'https://sanskritdocuments.org/sanskrit/yoga/',
  'Bhagavad Gita':         'https://sanskritdocuments.org/sanskrit/giitaa/',
  'Bhagavata Purana':      'https://sanskritdocuments.org/sanskrit/purana/',
  'Manu Smriti':           'https://sanskritdocuments.org/sanskrit/dharma/',
  'Taittiriya Upanishad':  'https://sanskritdocuments.org/sanskrit/upanishhat/',
  // Sadvritta and Modern application don't have a single canonical URL.
  'Sadvritta tradition':   'https://sanskritdocuments.org/sanskrit/ayurveda/',
};

export function getAyurvedaSourceUrl(source) {
  if (!source) return null;
  let url = null, bestLen = 0;
  for (const [text, u] of Object.entries(AYURVEDA_SOURCES)) {
    if (source.includes(text) && text.length > bestLen) {
      url = u;
      bestLen = text.length;
    }
  }
  return url;
}
