/**
 * Get today's Gita sloka (rotates daily based on day of month)
 */
export function getTodayGitaSloka(date = new Date()) {
  const dayOfMonth = date.getDate();
  const index = (dayOfMonth - 1) % GITA_SLOKAS.length;
  return GITA_SLOKAS[index];
}

/**
 * Get sloka by chapter and verse
 */
export function getGitaSloka(chapter, verse) {
  return GITA_SLOKAS.find(s => s.chapter === chapter && s.verse === verse) || null;
}

/**
 * Get all slokas for a theme
 */
export function getSlokasByTheme(keyword) {
  const kw = keyword.toLowerCase();
  return GITA_SLOKAS.filter(s =>
    s.keywords.some(k => k.includes(kw)) ||
    s.theme.toLowerCase().includes(kw)
  );
}

/**
 * Get all slokas from a chapter
 */
export function getSlokasByChapter(chapter) {
  return GITA_SLOKAS.filter(s => s.chapter === chapter);
}
